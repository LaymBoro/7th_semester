#pragma once
#include <iostream>
#include <sstream>


template<typename T>
class Node {
private:
    T data;
    Node<T> *left, *right, *parent;

public:
    Node() = default;
    Node(const Node<T>& other) = delete;
    Node(Node<T>&& other) noexcept = default;
    explicit Node(T data): left(nullptr), right(nullptr), parent(nullptr) { this->data = data; }
    ~Node() = default;
    T getData() { return this->data; }
    Node<T>* getLeft() { return this->left; }
    Node<T>* getRight() { return this->right; }
    Node<T>* getParent() { return this->parent; }
    void setLeft(Node<T> *child) { this->left = child; }
    void setRight(Node<T> *child) { this->right = child; }
    friend std::ostream& operator<<(std::ostream& out, const Node<T>& node){
        std::stringstream buffer{};
        buffer << node.data;
        out << buffer.str();
        return out;
    }
    bool operator==(Node<T> const& other) const { return data == other.data; }
    bool operator!=(Node<T> const& other) const { return data != other.data; }
    bool operator>=(Node<T> const& other) const { return data >= other.data; }
    bool operator<=(Node<T> const& other) const { return data <= other.data; }
};
